//switch
//if é faz uma série de verificações distintas
//swtich compara a mesma expressão ou variável com várias opções
const fruta = "banana"
switch(fruta){
    case "banana":
        console.log("banana é a fruta")
        break
    case "laranja":
        console.log("laranja é a fruta")
        break
    case "maçã":
        console.log("maçã é a fruta")
        break
    default:
        console.log("sem fruta")
        break
}

//diferença de while e if
//while tem uma condição para continuar
//if continua um determinada de vezes